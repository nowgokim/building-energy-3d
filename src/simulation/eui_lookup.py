def lookup_eui(
    archetype: str,
    vintage: str,
    city: str = "seoul",
) -> EUIStats | None:
    """(archetype, vintage, city) → EUIStats. city 불일치 시 seoul 폴백."""
    key = (archetype, vintage, city)
    if key in EUI_LOOKUP:
        return EUI_LOOKUP[key]
    # city 폴백: seoul
    key_seoul = (archetype, vintage, "seoul")
    return EUI_LOOKUP.get(key_seoul)
