def lookup_eui(
    archetype: str,
    vintage: str,
    city: str = "seoul",
) -> EUIStats | None:
    """(archetype, vintage, city) → EUIStats.

    Korean_BB에 city가 없으면 ems_transformer E0 도시 보정계수(× Seoul)를 적용.
    그것도 없으면 Seoul 그대로 반환.
    """
    key = (archetype, vintage, city)
    if key in EUI_LOOKUP:
        return EUI_LOOKUP[key]

    # Seoul 기준 데이터 조회
    key_seoul = (archetype, vintage, "seoul")
    seoul_stats = EUI_LOOKUP.get(key_seoul)
    if seoul_stats is None:
        return None

    # ems_transformer E0 도시 보정계수 적용 (Korean_BB 미지원 도시)
    try:
        from src.simulation.city_eui_base import get_city_factor
        factor = get_city_factor(archetype, city)
    except ImportError:
        factor = 1.0

    if factor == 1.0:
        return seoul_stats

    return {
        "median": round(seoul_stats["median"] * factor, 1),
        "p10":    round(seoul_stats["p10"] * factor, 1),
        "p90":    round(seoul_stats["p90"] * factor, 1),
        "n":      seoul_stats["n"],
    }
